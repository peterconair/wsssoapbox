package org.wsssoapbox.bean.model.soap;

public class MimeHeaderBean {
	/**
	 * @uml.property  name="post"
	 */
	private String post;
	/**
	 * @uml.property  name="acceptEncoding"
	 */
	private String acceptEncoding;
	/**
	 * @uml.property  name="contentType"
	 */
	private String contentType;
	/**
	 * @uml.property  name="sOAPAction"
	 */
	private String SOAPAction;
	/**
	 * @uml.property  name="userAgent"
	 */
	private String userAgent;
	/**
	 * @uml.property  name="host"
	 */
	private String host;

	/**
	 * @return
	 * @uml.property  name="post"
	 */
	public String getPost() {
		return post;
	}

	/**
	 * @param post
	 * @uml.property  name="post"
	 */
	public void setPost(String post) {
		this.post = post;
	}

	/**
	 * @return
	 * @uml.property  name="acceptEncoding"
	 */
	public String getAcceptEncoding() {
		return acceptEncoding;
	}

	/**
	 * @param acceptEncoding
	 * @uml.property  name="acceptEncoding"
	 */
	public void setAcceptEncoding(String acceptEncoding) {
		this.acceptEncoding = acceptEncoding;
	}

	/**
	 * @return
	 * @uml.property  name="contentType"
	 */
	public String getContentType() {
		return contentType;
	}

	/**
	 * @param contentType
	 * @uml.property  name="contentType"
	 */
	public void setContentType(String contentType) {
		this.contentType = contentType;
	}

	/**
	 * @return
	 * @uml.property  name="sOAPAction"
	 */
	public String getSOAPAction() {
		return SOAPAction;
	}

	/**
	 * @param sOAPAction
	 * @uml.property  name="sOAPAction"
	 */
	public void setSOAPAction(String sOAPAction) {
		SOAPAction = sOAPAction;
	}

	/**
	 * @return
	 * @uml.property  name="userAgent"
	 */
	public String getUserAgent() {
		return userAgent;
	}

	/**
	 * @param userAgent
	 * @uml.property  name="userAgent"
	 */
	public void setUserAgent(String userAgent) {
		this.userAgent = userAgent;
	}

	/**
	 * @return
	 * @uml.property  name="host"
	 */
	public String getHost() {
		return host;
	}

	/**
	 * @param host
	 * @uml.property  name="host"
	 */
	public void setHost(String host) {
		this.host = host;
	}

}
